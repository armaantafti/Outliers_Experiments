# Compute Euclidean Distance between two points
def computeDistance(point1, point2):
    return (sum((point1[i] - point2[i])**2 for i in range(len(point1)))) ** 0.5

# Build distance dictionary for all point pairs
def buildDistanceDictionary(points):
    distance_dict = {}
    for i in range(len(points)):
        for j in range(i, len(points)):
            point_i = tuple(points[i])
            point_j = tuple(points[j])
            distance = computeDistance(point_i, point_j)
            distance_dict[(point_i, point_j)] = distance
            distance_dict[(point_j, point_i)] = distance
    return distance_dict

# Solve LP for a given radius to test feasibility
def solve_k_center_LP(points, k, radius, distance_dict):
    n = len(points)
    point_indices = range(n)
    
    # Create LP problem
    prob = pulp.LpProblem("KCenter", pulp.LpMinimize)
    
    # LP variables (relaxed)
    y_vars = pulp.LpVariable.dicts("y", point_indices, lowBound=0, upBound=1, cat="Continuous")
    x_vars = pulp.LpVariable.dicts("x", (point_indices, point_indices), lowBound=0, upBound=1, cat="Continuous")
    
    # Each point j must be assigned to at least one feasible center
    for j in point_indices:
        feasible_centers = [i for i in point_indices if distance_dict[(tuple(points[i]), tuple(points[j]))] <= radius]
        prob += pulp.lpSum([x_vars[i][j] for i in feasible_centers]) >= 1, f"Cover_{j}"
    
    # Assignment only if center is opened
    for i in point_indices:
        for j in point_indices:
            prob += x_vars[i][j] <= y_vars[i], f"Link_{i}_{j}"
    
    # At most k centers can be opened
    prob += pulp.lpSum([y_vars[i] for i in point_indices]) <= k, "Center_Limit"
    
    # Dummy objective
    prob += 0

    # Solve with CBC solver (fast)
    solver = pulp.PULP_CBC_CMD(msg=0, timeLimit=30)  # 30 sec timeout to avoid hanging
    result = prob.solve(solver)
    
    if pulp.LpStatus[prob.status] == 'Optimal':
        print("\n📍 Fractional center openings (yᵢ > 0.01):")
        for i in point_indices:
            val = y_vars[i].varValue
            if val is not None and val > 0:
                print(f"  Point {i}: y = {val:.3f}")
        return True
    else:
        return False

# Binary Search using LP feasibility test
def findMinimumFeasibleRadius_LP(points, num_disks):
    full_distance_dict = buildDistanceDictionary(points)
    distance_values_sorted = sorted(set(full_distance_dict.values()))
    
    left = 0
    right = len(distance_values_sorted) - 1
    best_radius = None
    
    while left <= right:
        mid_idx = (left + right) // 2
        radius_candidate = distance_values_sorted[mid_idx]
        
        print(f"⏳ Testing radius {radius_candidate:.5f} ...")
        feasible = solve_k_center_LP(points, num_disks, radius_candidate, full_distance_dict)
        
        if feasible:
            print(f"✅ Radius {radius_candidate:.5f} is feasible.")
            best_radius = radius_candidate
            right = mid_idx - 1
        else:
            print(f"❌ Radius {radius_candidate:.5f} is NOT feasible.")
            left = mid_idx + 1
    
    return best_radius if best_radius is not None else "No feasible radius found"

